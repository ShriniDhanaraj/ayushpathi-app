/** Get translation strings for a given language code. Falls back to English. */
export function getTranslations(lang: LangCode): Translations {
  return STRINGS[lang] ?? STRINGS['EN']
}

/** Shorthand accessor */
export function t(key: keyof Translations, lang: LangCode): string {
  return getTranslations(lang)[key]
}

/** Replace {n} and {total} in stepOf string */
export function tStep(n: number, total: number, lang: LangCode): string {
  return getTranslations(lang).stepOf
    .replace('{n}', String(n))
    .replace('{total}', String(total))
}

